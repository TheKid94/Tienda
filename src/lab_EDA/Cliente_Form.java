    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        
        Cliente cliente = new Cliente(Integer.parseInt(txtDNI.getText()),txtNombre.getText(),txtApellidoP.getText(),txtApellidoM.getText());
        lista.add(cliente);
        cli.GuardarDatosClienteTxt(lista);
        
        mostrarDatos();
        
        txtDNI.setText("");
        txtNombre.setText("");
        txtApellidoP.setText("");
        txtApellidoM.setText("");
        
        
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        for (int i = 0; i < lista.size(); i++) {
            if(dni.equals(String.valueOf(lista.get(i).getDNI()))){
            lista.remove(i);
            }
        }
        cli.GuardarDatosClienteTxt(lista);
        mostrarDatos();
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        txtDNI.setText(dni);
        txtNombre.setText(nombre);
        txtApellidoP.setText(apellidop);
        txtApellidoM.setText(apellidom);
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnGuardaModificacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardaModificacionActionPerformed
        
        for (int i = 0; i < lista.size(); i++) {
            if(dni.equals(String.valueOf(lista.get(i).getDNI()))){
            lista.get(i).setDNI(Integer.parseInt(txtDNI.getText()));
            lista.get(i).setNombre(txtNombre.getText());
            lista.get(i).setApellidoPa(txtApellidoP.getText());
            lista.get(i).setApellidoMa(txtApellidoM.getText());
            }
        }
        
        cli.GuardarDatosClienteTxt(lista);
        
        txtDNI.setText("");
        txtNombre.setText("");
        txtApellidoP.setText("");
        txtApellidoM.setText("");
        

        mostrarDatos();
        
    }//GEN-LAST:event_btnGuardaModificacionActionPerformed

    public void mostrarDatos(){
        String matriz[][] = new String[lista.size()][4];
        Collections.sort(lista);
        listaux.clear();
        for(Cliente arrCli: lista){
            listaux.add(arrCli);
        }
        for (int i = 0; i < lista.size(); i++) {
            
            matriz[i][0]=String.valueOf(listaux.get(i).getDNI());
            matriz[i][1]=listaux.get(i).getNombre();
            matriz[i][2]=listaux.get(i).getApellidoPa();
            matriz[i][3]=listaux.get(i).getApellidoMa();
                              
        }
        
        jTableAlumno.setModel(new javax.swing.table.DefaultTableModel(matriz, new String [] {"DNI","Nombre","Apellido Paterno","Apellido Materno"}) );
        
    }
            
    
